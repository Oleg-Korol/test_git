# ========================================================================================================================


predlojenie="Hello Minsk"
peremennaya=" ! "   #создаем строкавую переменную
predlojenie1 = predlojenie[:5] + peremennaya + predlojenie[6:]   #соединяем слова
predlojenie2 = predlojenie1.split()  # разделяем предложение на слова
predlojenie2[0], predlojenie2[1], predlojenie2[2]= predlojenie2[2], predlojenie2[1], predlojenie2[0] #меняем слова местами
assert predlojenie2 == ['Minsk', '!', 'Hello']  #проверка
predlojenie3= " ".join(predlojenie2)            #преобразуем обратно в строку
assert predlojenie3 =="Minsk ! Hello"           #проверка
print(predlojenie3)

