# 1.Ввести предложение, состоящее из двух слов.
# 2.Поменять местами слова, добавить "!" в начало и конец слова.
# 3.Разделить 3 символами (" ", "!", " ")
# 4.Пример: "hello world" --> "world ! hello"
# 5.Задание выполнить разными способами форматирования.


# ========================================================================================================================
